import numpy as np

arr1 = np.array([3, 2, 0, 1])  # Sort the array numerically
print("Sort the array numerically")
print(np.sort(arr1))
print("********************************************")

arr2 = np.array(['banana', 'cherry', 'apple'])   # Sort the array alphabetically
print("Sort the array alphabetically")
print(np.sort(arr2))
print("********************************************")

arr3 = np.array([True, False, True])   # Sort a boolean array
print("Sort a boolean array")
print(np.sort(arr3))
print("********************************************")


arr4 = np.array([[3, 2, 4], [5, 0, 1]])   # Sort a 2-D array
print("Sort a 2-D array")
print(np.sort(arr4))
print("********************************************")






